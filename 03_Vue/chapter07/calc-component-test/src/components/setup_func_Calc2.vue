<script>
import { ref } from "vue";

export default {
  name: "Calc2",
  setup: function () {
    const x = ref(10);
    const y = ref(20);
    const result = ref(30);

    const calcAdd = () => {
      // result.value = x.value + y.value;
      result = x + y;
    };

    return { x, y, result, calcAdd };
  },
};
</script>

<template>
  <div>
    <h3>CALCULATION 2</h3>
    X: <input type="text" v-model.number="x" />
    <br />
    Y: <input type="text" v-model.number="y" />
    <br />
    <button @click="calcAdd">계산</button>
    <div>결과: {{ result }}</div>
  </div>
</template>
